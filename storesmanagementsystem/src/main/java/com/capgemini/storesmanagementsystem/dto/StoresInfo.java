package com.capgemini.storesmanagementsystem.dto;

public class StoresInfo {
	private int productId;
	private int numOfItems;
	private int storesId;
	private int minStockPresent;
	public int getProductId() {
		return productId;
	}
	public void setProductId(int productId) {
		this.productId = productId;
	}
	public int getNumOfItems() {
		return numOfItems;
	}
	public void setNumOfItems(int numOfItems) {
		this.numOfItems = numOfItems;
	}
	public int getStoresId() {
		return storesId;
	}
	public void setStoresId(int storesId) {
		this.storesId = storesId;
	}
	public int getMinStockPresent() {
		return minStockPresent;
	}
	public void setMinStockPresent(int minStockPresent) {
		this.minStockPresent = minStockPresent;
	}
	

}
